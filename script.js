var selectedRegulation = '';
var selectedCourse = '';

function openRegulationModal() {
    var modal = document.getElementById('regulation-modal');
    modal.style.display = 'block';
}

function openCourseModal() {
    if (selectedRegulation === '') {
        alert('Please select a regulation first.');
    } else {
        var modal = document.getElementById('course-modal');
        modal.style.display = 'block';
    }
}

function displaySubjectDetails() {
    if (selectedRegulation !== '' && selectedCourse !== '') {
        var subjectDetails = document.getElementById('subject-details');
        subjectDetails.innerHTML = '';

        var semesterDetails = courseData[selectedRegulation][selectedCourse];

        var allElements = []; // Use an array to temporarily store all the elements

        for (var semester in semesterDetails) {
            var semesterLabel = document.createElement('div');
            semesterLabel.className = 'semester-label';
            semesterLabel.textContent = semester;
            allElements.push(semesterLabel); // Add to the array

            var table = document.createElement('table');
            table.innerHTML = '<tr><th>Subject Code</th><th>Subject</th><th>Credits</th></tr>';

            for (var i = 0; i < semesterDetails[semester].length; i++) {
                var row = document.createElement('tr');
                for (var key in semesterDetails[semester][i]) {
                    var cell = document.createElement('td');
                    cell.textContent = semesterDetails[semester][i][key];
                    row.appendChild(cell);
                }
                table.appendChild(row);
            }
            allElements.push(table); // Add to the array
        }

        // Now append all the elements from the array to subjectDetails
        allElements.forEach(element => {
            subjectDetails.appendChild(element);
        });
    }
}

